export function resolveLocale(language: AppLanguage | undefined, systemLocale?: string): AppLocale {
  if (language && language !== "system") return language;
  const normalized = (systemLocale ?? "").toLowerCase();
  if (normalized.startsWith("en")) return "en-US";
  if (normalized.startsWith("ja")) return "ja-JP";
  if (normalized.startsWith("ko")) return "ko-KR";
  return "zh-CN";
}

export function translateText(locale: AppLocale, text: string): string {
  if (locale === "zh-CN") return text;
  return translations[text]?.[locale] ?? text;
}

export function interpolate(template: string, params?: Record<string, string | number>): string {
  if (!params) return template;
  return Object.entries(params).reduce((result, [key, value]) => result.replaceAll(`{${key}}`, String(value)), template);
}

export function translate(locale: AppLocale, text: string, params?: Record<string, string | number>): string {
  return interpolate(translateText(locale, text), params);
}
